                            # Get Feed, Write File
                            $feedcontent = file_get_contents($feedURL);
                            $fp = fopen($cachefile, "w");
                            fwrite($fp, $feedcontent);
                            fclose($fp);
                            return $feedcontent;
                        }

                        function checkCache()
                        {
                            global $cachefile;

                            $cachetime = 60 * 60 * 3; # 10800min = 3h
                            if(is_file($cachefile) and (time() - filemtime($cachefile)) > $cachetime)
                            {
                                return readfile($cachefile);
                            }
                            else
                            {
                                return getfeed();
                            }
                        }

