                        <dl>
                            <dt>Frage: <br />
                                Clansuite ist doch ein Content-Management-System für Clans und eSport-Team. Wir möchten aber ein System, welches auch als normales CMS
                                funktioniert, also auch für andere Zwecke eingesetzt werden kann. Ist das mit Clansuite machbar?
                            </dt>
                            <dd><br /> Antwort:
                                <p>
                                    Ja, Clansuite ist modular aufgebaut. Je nach Blickwinkel kann man sich Clansuite einerseits als ein normales
                                    CMS vorstellen, dass um typische Clanmodule, wie z.B. Matches, Serverviewer oder Teamspeakviewer ergänzt wird.
                                    Anderseits kann man sich aber auch ein typisches Clan-CMS vorstellen, bei dem die Möglichkeit besteht,
                                    die Clanmodule einfach wegzulassen.
                                </p>
                            </dd>

                            <dt>Frage: <br />
                                Bei Clansuite werden ja viele fremde Bibliotheken, also fremder Code eingesetzt? Ist das nicht eher schlecht?
                            </dt>
                            <dd><br /> Antwort:
                                <p>
                                    Die von uns ausgewählten Klassen sind durchweg leistungsfähige Open-Source Bibliotheken,
                                    die auch unseren Lizenzanforderungen genügen. Sie werden zudem in vielen anderen Applikationen erfolgreich eingesetzt.
                                    Je nach Standpunkt kann man das schlecht oder gut finden - es ist eine müßige Frage.
                                    Ich vertrete die Auffassung, dass man nicht jede Zeile Code in einem CMS Projekt selbst geschrieben haben muss.
                                    Wer die Zeit besitzt alles selbst zu schreiben, kann dies jedoch gerne tun. Wir halten niemanden davon ab, in
                                    eigenen Projekten anders zu verfahren.
                                </p>
                            </dd>

                            <dt>Frage: <br />
                                Wir möchten ein schlankes, schnelles System. Erfüllt Clansuite diese Kriterien?
                            </dt>
                            <dd><br /> Antwort:
                                <p>
                                    Wer ein schlankes, schnelles System will, der ist mit dem Einsatz von Clansuite falsch beraten!
                                    Clansuite benutzt viele große Bibliotheken, wie z.B. Smarty und Doctrine und ist daher grundsätzlich nicht schlank angelegt.
                                    Diese Bibliotheken vereinfachen die Entwicklungstätigkeiten, indem sie kraftvolle Unterstützung liefern.
                                    Allerdings wirkt sich das Laden und Verwenden dieser Bibliotheken negativ auf den Speicherverbrauch (Memory-Footprint) aus und erzeugt Systemlast.
                                    Im Rahmen der Möglichkeiten versuchen wir, durch Profiling den Code an sich und durch Caching die Geschwindigkeit zu verbessern.
                                    Caching kann dabei in den Bereichen Templates, Datenbank und Speicher zum Einsatz kommen.
                                </p>
                            </dd>

                            <dt>Frage: <br />
                                Wir möchten kein oder so wenig wie möglich Javascript eingesetzt sehen? Erfüllt Clansuite dieses Kriterium?
                            </dt>
                            <dd><br /> Antwort:
                                <p>
                                    Teilweise! Erstens ist der Einsatz von Javascript aus den modernen Internetapplikationen nicht mehr wegzudenken
                                    und der gänzliche Verzicht darauf, eine müßige, gleichsam unnötige Forderung und Selbstbeschränkung.
                                    Zweitens setzt Clansuite auf die Erhöhung der Nutzbarkeit für den User (Useability) durch sinnvollen Einsatz von Javascript.
                                    Regelmäßig greifen wir daher auf bekannte Javascript-Frameworks (wie z.B. jQuery oder mooTools) zurück, um in einigen
                                    Bereichen des Backends die Useability und auch optische Spielereien und Effekte, wie z.B. sanfte Einblendungen,
                                    Highlighting, DropDowns und Moveables einzusetzen. Drittens können Frontend-Themes durch Benutzer erstellt werden:
                                    hierbei obliegt dem Nutzer selbst die Entscheidung, ob er Javascript einsetzen möchte oder nicht.
                                    Kleinere Verpflichtungen zum Einsatz von JS sind dabei zu berücksichtigen, wie z.B. beim Login.
                                </p>
                            </dd>

                            <dt>Frage: <br />
                                Darf man fragen, wie lange Ihr noch für die nächste Version bzw. Veröffentlichung braucht?
                            </dt>
                            <dd><br /> Antwort:
                                <p>
