                    <div id="page-features" class="accordion hide">
                        <h2 class="headerstyle">
                            <span class="sprite emblem-system"></span>
                            <span class="headerstyle-text">Leistungsmerkmale</span>
                        </h2>
                        <p>Dies ist ein Überblick über den Funktionsumfang und die Leistungsmerkmale von Clansuite. Unsere <a href="http://trac.clansuite.com/roadmap/">Roadmap</a> zeigt Ihnen, woran wir augenblicklich arbeiten.</p>
                        <h3>Wesentliche Leistungsmerkmale</h3>
                        <p>Clansuite ist eine in PHP geschriebene Webapplikation für E-Sportteams und Clans.</p>
                        <p>Beim Entwurf wurde großen Wert auf Einfachheit und Übersichtlichkeit gelegt - gerade auch, weil der neueste Schrei in Sachen Webtechnologie verwendet wird.</p>
                        <p>Die Anwendung ist objektorientiert aufgebaut und als einfach erweiterbares Framework konzipiert. Das CMS stützt sich dabei auf eine ganze Reihe von hervorragenden Open-Source Bibliotheken.</p>
                        <dl>
                            <dt>Objektorientierte Webapplikation basierend auf Koch Framework</dt>
                            <dd>Clansuite ist eine objektorientierte Webapplikation in der zahlreiche Entwurfsmuster verwendet werden.</dd>
                            <dd>Zum Einsatz kommen: das Web-MVC Muster, sowie ein Frontcontroller mit Intercepting-Filters.</dd>
                            <dd>Er leitet die URL-Parameter an die entsprechenden Controller und ermöglicht so die Ausgabe von Views durch die angeforderten Actions.</dd>
                            <dd>Weitere verwendete Entwurfsmuster sind: Singleton, Factory, Two-Step View, Facade, Adapter,</dd>
                            <dd>Iterator und Inversion of Control (Ioc). (Genug der Buzzwords.)</dd>
                            <dd>Die Abhängigkeitsauflösung zwischen Klassen erfolgt mittels der Dependency-Injection Bibliothek Phemto.</dd>
                            <dt>Objekt Relationale Abbildung (ORM) via Doctrine</dt>
                            <dd>Als Datenbankabstraktionslayer und objektrelationales Abbildungswerkzeug kommt PHPDoctrine zum Einsatz.</dd>
                            <dd>Doctrine setzt auf PHP's PDO auf und unterstützt die folgenden Datenbanken:</dd>
                            <dd>MySQL (sqlite, pgsql, odbc, oci, informix, firebird, dblib).</dd>
                            <dt>Sicherheit</dt>
                            <dd>Durch Verwendung von Doctrine und PDO wird die Wahrscheinlichkeit von SQL Injections begrenzt.</dd>
                            <dd>Zu keinem Zeitpunkt kennt das System ein Benutzerpassword. Es ist lediglich der Hash verfügbar.</dd>
                            <dd>Die Hashes sind zudem "SALT"ed für jedes Benutzerkonto - somit wird die Wahrscheinlichkeit eines erfolgreichen Brute-Force Angriffs abgemildert.</dd>
                            <dd>Es gibt definierbare Benutzerrechte für jede Handlung. Einstellbar über das Adminmenü.</dd>
                            <dd>Abgeleitete Benutzerrechte von Bereichen / Gruppen / Benutzerkonten.</dd>
                            <dd>Ein Inputfilter prüft die Validität der eingehenden Variablen.</dd>
                            <dd>Jeder Schritt kann ins Logbuch aufgenommen werden - eine Benachrichtigung des Admins bei besonderen Ereignissen ist möglich.</dd>
                            <dd>Ein leistungsfähiges Einbruchserkennungsystem mit Reportsystem rundet die Sicherheitsfeatures ab.</dd>
                            <dt>AJAX</dt>
                            <dd>Es werden Web 2.0 Ajax-Bibliotheken unterstützt.</dd>
                            <dd>Hauptsächlich werden die Effekt-Bibliotheken JQuery und Mootools und ihre Erweiterungen verwendet.</dd>
                            <dt>Template Engines</dt>
                            <dd>Das System kann mit oder ohne Template Engine betrieben werden.</dd>
                            <dd>Eine Ausgabe mittels nativen PHP-Ausgabehandling ist möglich.</dd>
                            <dd>Standardmäßig kommt Smarty zum Einsatz.</dd>
                            <dd>Die Verwendung von Smarty als Template Engine bietet ein Maximum an Flexibilität bei der Gestaltung von Templates.</dd>
                            <dt>Themes / Schablonen</dt>
                            <dd>Um das Aussehen der Website nach Belieben anzupassen und zu verändern, kannst Du auf Themes bzw. Schablonen zurückgreifen.</dd>
                            <dd>Die allgemeine Rahmenschablone, sowie sämtliche Modulschablonen sind veränderbar. Zugleich kannst Du aus dem System heraus viele weitere herunterladen und automatisch installieren.</dd>
                            <dd>Der Administrationsbereich ist ebenfalls eine Theme und damit komplett veränderbar.</dd>
                            <dd>Das Wechseln des Aussehens kann per Config, per URL Paramter oder Usersetting vorgenommen werden.</dd>
                            <dt>Sprachen und Lokalisierung</dt>
                            <dd>Clansuite verwendet eine gettext() basierte Lokalisationseinstellung um Texte übersetzt anzuzeigen.</dd>
                            <dd>Es werden verschiedenste Sprachen (i18n) untersützt. Die Übersetzungen werden dabei einerseits von der Community, andererseits von einer internationalen Übersetzerwebsite geliefert.</dd>
                            <dd>Außerdem ist ein nützlicher Übersetzungeditor im Adminbereich integriert.</dd>
                            <dt>Rechte</dt>
                            <dd>Clansuite setzt eine fortgeschrittene Rechte- und Benutzerverwaltung ein. Stichwörter: ACL und RBACL.</dd>
                            <dd>Es gibt Gruppen, Bereiche und Einzelrechte.</dd>
                            <dd>Es findet eine flexible Einbindung des Rechtesystems in den jeweiligen View statt,</dd>
                            <dd>wodurch eine rechteabhängige Darstellung von Benutzernavigationen und Menüs möglich ist.</dd>
                            <dt>Installation</dt>
                            <dd>Clansuite verfübt über eine einfache Installation. Sie kann mittels einer einzigen Datei (Webinstaller) durchgeführt wird.</dd>
                            <dd>Der Webinstaller lädt alle erforderlichen Module und Dateien auf den Webserver.</dd>
                            <dd>Im Anschluß ermöglicht eine "Schritt für Schritt" Installation einen sehr einfachen Weg der Einrichtung des eigentlichen Systems.</dd>
                            <dt>Module</dt>
                            <dd>News mit Archiv / Blog </dd>
                            <dd>Shoutbox</dd>
                            <dd>Gästebuch für die Website und für jedes Teammember</dd>
                            <dd>Forum (Bulletin Board Style)</dd>
                            <dd>Matches (automatischer Abgleich mit den Ergebnissen der jeweiligen eSport-Liga)</dd>
                            <dd>Bildergallery</dd>
                            <dd>Gameserver-Viewer (instant query)</dd>
                            <dd>Auszeichnungen</dd>
                            <dd>Teamspeak 2 und 3, Mumble, Ventrilo Viewer</dd>
                            <dd>Kalender</dd>
                            <dd>Statische Seiten erstellen</dd>
                            <dd>...bald mehr..!</dd>
                            <dd>Erweiterungsvorschläge sind erwünscht! Bitte verwende unseren Bugtracker oder das Forum !</dd>
                        </dl>
