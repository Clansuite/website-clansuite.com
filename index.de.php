                        <h2 class="headerstyle">
                            <span class="sprite glade-3"></span>
                            <span class="headerstyle-text">Einführung - Was ist Clansuite?</span>
                        </h2>
                        <p>Jeder Clan und jedes eSport-Team braucht eine gelungene Webpräsentation.</p>
                        <p>Clansuite ist der Motor dieser Webpräsentation!</p>
                        <p>Clansuite ist ein Content Management System, mit welchem Du schnell und einfach eine Clanwebseite aufsetzen und deren Inhalte verwalten kannst.</p>
                        <p>Es entspricht dem aktuellen Stand der Web-Technik und ist speziell auf eSport-Teams zugeschnitten.</p>
                        <p>Es wurde für eine effektive Verwaltung der Team-Ressourcen (Personal, Arbeitsabläufe und Informationen) entworfen, um somit bessere Wettbewerbsergebnisse zu erzielen.</p>
                        <p>Aufgrund seines modularen Aufbaus ist es besonders für jene Leute gedacht, die auf der Suche nach einem funktionell umfangreichen aber dennoch einfach zu handhabenden Content Management System sind.</p>
                        <p>Somit stellt Clansuite auch für den Webauftritt von Sportmannschaften aller Art, Vereinen und Unternehmen eine optimale Lösung dar.</p>
