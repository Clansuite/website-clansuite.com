                                <p>
                                Sometimes! Firstly, the use of JavaScript has become an integral in modern web applications and complete abandonment is an almost
                                unnecessary exposure and self-restraint. Secondly, Clansuite aims to increase the usability for the user by appropriate use of
                                JavaScript. Therefore we regularly use known Javascript frameworks (such as jQuery or MooTools) in areas of the backend, to increase
                                useability and create optical illusions and effects, such as soft overlays, highlighting, drop-downs and moveables. Thirdly, frontend
                                themes are created by users: so the user is responsible for his work and the decision whether to use or not use Javascript. Smaller
