                                In Clansuite many third party libraries are used? Isn't that rather bad?
                            </dt>
                            <dd><br /> Answer:
                                <p>
                                 Our selected classes are all powerful open-source libraries that also satisfy our license requirements. They are used successfully in many
                                 other applications. Depending on your view, you can find that bad or good - it is a moot question. I'm of the opinion that one must not
                                 have written every line of code in a CMS project oneself. Whoever has the time to write everything themself, may do it. We hold no one
                                 ack to proceed in own projects differently.
                                </p>
                            </dd>

                            <dt>Question: <br />
                                We want a lean, fast content management system. Does Clansuite meet these criteria?
                            </dt>
                            <dd><br /> Answer:
                                <p>
                                Who searches for a sleek and extremly fast system is ill-advised in the use of Clansuite! Clansuite uses many large libraries such as
                                Smarty 3 and Doctrine 2, and is therefore generally not architectured slimly. None the less these libraries facilitate development
                                activities by providing powerful support for developers. However, the impact of loading and storing these libraries has a negative effect
                                on memory usage (memory footprint) and generates system load. In the scope of our possibilities we try to improve by profiling the code
                                itself and the general performance by caching. Caching occurs in the areas of templates, database and storage used.
                                </p>
                            </dd>

                            <dt>Question: <br />
                                 We don't want to see any javascript used or as less as possible? Does Clansuite meet this criterion?
                            </dt>
