                            <dd>Iterator, Dependency Injection / Inversion of Control (Ioc) via Phemto</dd>
                            <dd>Event-driven architecture</dd>
                            <dd>Native AJAX and REST support</dd>
                            <dd>Authentication and Authorization with session based RBACL</dd>
                            <dd>Object Relation Mapping with PDO via Doctrine 2</dt>
                            <dd>I18n support using UTF8 and Gettext</dd>
                            <dd>Form generation with client and server side validation and data sanitization</dd>
                            <dd>Two-step view with template based views for layout, modules and partials/widgets.</dd>
                            <dd>Doctrine2 acts as powerful Database Abstraction Layer (DBAL) and Object-Relation Mapping (ORM) Tool.</dd>
                            <dd>PDO itself supports the following databases: MySQL (sqlite, pgsql, odbc, oci, informix, firebird, dblib)</dd>
